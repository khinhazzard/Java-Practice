
public class L02_variables {
	public static void main(String args[]){
		double num; //declaring a variable
		num = 5.28; //assigning value
		
		System.out.print("I want ");		//shows a string of text that stays on the same line
		System.out.print(num);				//shows a value of variable
		System.out.println(" movies");		//the end of string moves to a new line
		System.out.print("apples");
	}
}

